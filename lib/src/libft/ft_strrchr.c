char	*ft_strrchr(const char *s, int c)
{
	int	len;
	int	i;

	len = 0;
	while (s[len])
		len++;
	i = len;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	return (0);
}
